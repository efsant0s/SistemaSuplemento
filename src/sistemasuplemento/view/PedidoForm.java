     private void updateTableItens() {

        String[] columnNames = {"Ordem", "Produto", "Quantidade", "Preço", "Total"};

        DefaultTableModel tableModel = new DefaultTableModel(columnNames, 0);

        double valorTotal = 0;
        
        for (PedidoItem item : model.getItens()) {
            
            valorTotal += item.getValorTotal();
            
            Object[] data = {item.getOrdem(), item.getSuplemento().getNome(),
                item.getQuantidade(), item.getValorUnitario(), item.getValorTotal()};
            tableModel.addRow(data);
        }
        tbItensPedido.setModel(tableModel);
        
        edValorTotal.setText(FormaUtils.formatDecimal(valorTotal));
        
    }

