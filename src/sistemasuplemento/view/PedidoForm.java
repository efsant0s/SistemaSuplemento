    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
    if (tbItensPedido.getSelectedRow() != -1) {
            PedidoItem item = model.getItens().get(tbItensPedido.getSelectedRow());
            openFormItem(item);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
    if (tbItensPedido.getSelectedRow() != -1) {
            int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o item???");
            if (resp == JOptionPane.YES_OPTION) {
                model.getItens().remove(tbItensPedido.getSelectedRow());
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
    try {
            
            model.setNumero(FormaUtils.parseLong(edNumero.getText()));
            model.setEmissao(FormaUtils.parseDateWithHour(edEmissao.getText()));
            model.setAprovacao(FormaUtils.parseDateWithHour(edAprovacao.getText()));
            model.setCliente((Cliente) cbCliente.getSelectedItem());
            model.setValorTotal(FormaUtils.parseDecimal(edValorTotal.getText()));
            model.setFormaPagto((FormaPagto) cbFormaPagto.getSelectedItem());
            model.setCondicaoPagto((CondicaoPagto) cbCondicaoPagamento.getSelectedItem());
            if (model.getIdPedido() != 0) {
                dao.update(model);
            } else {
                dao.insert(model);
            }
            
            dispose();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Campos incorretos no formulário!!!");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

