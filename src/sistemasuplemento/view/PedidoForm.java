    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
    updateTableItens();

        cbCliente.removeAllItems();
        ClienteDao daoCliente = new ClienteDao();

        for (Cliente cli : daoCliente.findAll()) {
            cbCliente.addItem(cli);
        }
        
        cbCondicaoPagamento.removeAllItems();
        List<CondicaoPagto> condicao = daoCondicao.findAll();
        for (CondicaoPagto condicoes : condicao) {
            cbCondicaoPagamento.addItem(condicoes);
        }
        cbFormaPagto.removeAllItems();
        List<FormaPagto> forma = daoForma.findAll();
        for (FormaPagto formas : forma) {
            cbFormaPagto.addItem(formas);
        }
        cbCondicaoPagamento.setSelectedItem(model.getCondicaoPagto());  
        cbFormaPagto.setSelectedItem(model.getFormaPagto());
        
    }//GEN-LAST:event_formWindowActivated

