 public void setModel(Pedido model) {
        this.model = model;

        edNumero.setText(FormaUtils.formatLong(model.getNumero()));
        edEmissao.setText(FormaUtils.formatDateWithHour(model.getEmissao()));
        edAprovacao.setText(FormaUtils.formatDateWithHour(model.getAprovacao()));
        edValorTotal.setText(FormaUtils.formatDecimal(model.getValorTotal()));
        if (model.getCondicaoPagto()!= null) {
            CondicaoPagto condicao = daoCondicao.find(model.getCondicaoPagto().getId());
            cbCondicaoPagamento.setSelectedItem(condicao);
        }
        if (model.getFormaPagto()!= null) {
            FormaPagto forma = daoForma.find(model.getFormaPagto().getId());
            cbFormaPagto.setSelectedItem(forma);
        }
    }
    private void openFormItem(PedidoItem item) {
        if (formItem == null) {
            formItem = new PedidoItemForm();
        }
        formItem.setModel(item);
        formItem.setVisible(true);
    }
