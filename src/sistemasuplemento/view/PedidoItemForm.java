    private void edQuantidadeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_edQuantidadeFocusLost
    try {

            double valorTotal = FormaUtils.parseDecimal(edQuantidade.getText())
                    * FormaUtils.parseDecimal(edValorUnitario.getText());
            edValorTotal.setText(FormaUtils.formatDecimal(valorTotal));

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Campos incorretos no formulário de itens!!!");
        }
    }//GEN-LAST:event_edQuantidadeFocusLost

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
    SuplementoDao daoSuplemento = new SuplementoDao();
        cbSuplemento.removeAllItems();

        for (Suplemento suplemento : daoSuplemento.findAll()) {
            cbSuplemento.addItem(suplemento);
        }
        cbSuplemento.setSelectedItem(model.getSuplemento());
    }//GEN-LAST:event_formWindowActivated

    private void cbSuplementoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbSuplementoItemStateChanged
    Suplemento p = (Suplemento) cbSuplemento.getSelectedItem();
        if (p != null) {
            edValorUnitario.setText(FormaUtils.formatDecimal(p.getPreco()));
        }
    }//GEN-LAST:event_cbSuplementoItemStateChanged

