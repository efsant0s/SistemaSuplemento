    private void btSavlarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSavlarActionPerformed
     try {
            model.setSuplemento((Suplemento) cbSuplemento.getSelectedItem());
            model.setQuantidade(FormaUtils.parseDecimal(edQuantidade.getText()));
            model.setValorUnitario(FormaUtils.parseDecimal(edValorUnitario.getText()));
            model.setValorTotal(FormaUtils.parseDecimal(edValorTotal.getText()));

            dispose();
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Campos incorretos no formulário de itens!!!");
        }
    }//GEN-LAST:event_btSavlarActionPerformed

