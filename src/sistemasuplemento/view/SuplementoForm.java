public void setModel(Suplemento model) {
        this.model = model;
        
        edNome.setText(model.getNome());
        edPreco.setText(model.getPreco() + "");
        edCodigo.setText(model.getCodigo());
        edQuantidade.setText(model.getQuantidade() + "");
        if (model.getTipoSuplemento()!= null) {
            TipoSuplemento tipo = daoTipo.find(model.getTipoSuplemento().getId());
            cbTipo.setSelectedItem(tipo);
        }
        if (model.getMarcaSuplemento()!= null) {
            MarcaSuplemento marca = daoMarca.find(model.getMarcaSuplemento().getId());
            cbMarca.setSelectedItem(marca);
        }
    }
