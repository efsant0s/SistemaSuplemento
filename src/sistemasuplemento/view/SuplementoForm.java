    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        cbTipo.removeAllItems();
        List<TipoSuplemento> tipo = daoTipo.findAll();
        for (TipoSuplemento tipos : tipo) {
            cbTipo.addItem(tipos);
        }
        cbMarca.removeAllItems();
        List<MarcaSuplemento> marca = daoMarca.findAll();
        for (MarcaSuplemento Marcas : marca) {
            cbMarca.addItem(Marcas);
        }
        cbTipo.setSelectedItem(model.getTipoSuplemento());  
        cbMarca.setSelectedItem(model.getMarcaSuplemento());
        
    }//GEN-LAST:event_formWindowActivated

