    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        model.setNome(edNome.getText());
        model.setCodigo(edCodigo.getText());
        try {
            model.setPreco(FormaUtils.parseDecimal(edPreco.getText()));
        } catch (ParseException ex) {
           JOptionPane.showMessageDialog(this, "Coloque o Preco no formato 00,00");
        }
        model.setTipoSuplemento((TipoSuplemento)cbTipo.getSelectedItem());
        model.setMarcaSuplemento((MarcaSuplemento)cbMarca.getSelectedItem());
        model.setQuantidade(Double.parseDouble(edQuantidade.getText()));
        if (model.getIdProduto() != 0) {
            dao.update(model);
        } else {
            dao.insert(model);
        }
        dispose();
    }//GEN-LAST:event_btSalvarActionPerformed

