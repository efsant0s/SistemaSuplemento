private void openForm(Estado model) {
        if (form == null)
            form = new EstadoForm();
        form.setModel(model);
        form.setVisible(true);
    }

    private long getSelectedId() {
        if (tbEstados.getSelectedRow() == -1)
            return 0;
        
        Object valor = tbEstados.getModel().getValueAt(
                tbEstados.getSelectedRow(), 0);
        return (Long) valor;
    }
