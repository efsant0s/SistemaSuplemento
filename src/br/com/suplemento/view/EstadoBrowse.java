    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
     List<Estado> estados = dao.findAll();
    String[] columnNames = {"ID", "Código", "Nome"};
    DefaultTableModel Model = new DefaultTableModel(columnNames, 0);
    for (Estado estado : estados) {
            Object[] dados = new Object[]{estado.getIdEstado(), estado.getCodigo(),estado.getNome() };
            Model.addRow(dados);
        }
    tbEstados.setModel(Model);
    }//GEN-LAST:event_formWindowActivated

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
    Estado selecionado = dao.find(getSelectedId());
        if (selecionado != null)
            openForm(selecionado);
    
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
     if (getSelectedId() != 0) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o registro?");
            if (resposta == JOptionPane.YES_OPTION) {
                dao.delete(getSelectedId());
            }
        }
       
    }//GEN-LAST:event_btExcluirActionPerformed

