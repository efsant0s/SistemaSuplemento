    public void abrirJanela(String nome) {
        JFrame frame = null;
        if (janelas.containsKey(nome)) {
            frame = janelas.get(nome);
        } else {
            switch (nome) {
                case "FormaPagtoBrowse":
                    frame = new FormaPagtoBrowse();
                    break;
                case "CondicaoPagtoBrowse":
                    frame = new CondicaoPagtoBrowse();
                    break;
                
                default:
                    throw new AssertionError("Janela não existe");
            }
        }
        frame.setVisible(true);
        janelas.put(nome, frame);
    }

