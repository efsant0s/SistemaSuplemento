    private void openForm(Pedido model) {
        if (form == null) {
            form = new PedidoForm();
        }
        form.setModel(model);
        form.setVisible(true);
    }

    private Long getSelectedId() {
        if (tbPedidos.getSelectedRow() == -1) {
            return null;
        }

        Object valor = tbPedidos.getModel().getValueAt(
                tbPedidos.getSelectedRow(), 0);
        return (Long) valor;
    }
