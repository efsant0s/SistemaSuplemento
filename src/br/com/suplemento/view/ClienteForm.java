public void setModel(Cliente model) {
        this.model = model;
        
        edNome.setText(model.getNome());
        edCodigo.setText(model.getCodigo());
        edTelefone.setText(model.getTelefone());
        edEmail.setText(model.getEmail());
        edEndereco.setText(model.getEndereco());
        edDataNascimento.setText(model.getDataNascimento());
        if (model.getCidade()!= null) {
            Cidade cidade = daoCidade.find(model.getCidade().getIdCidade());
            cbCidade.setSelectedItem(cidade);
        }
    }
