    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        model.setNome(edNome.getText());
        model.setCodigo(edCodigo.getText());
        model.setTelefone(edTelefone.getText());
        model.setEmail(edEmail.getText());
        model.setEndereco(edEndereco.getText());
        model.setDataNascimento(edDataNascimento.getText());
        model.setCidade((Cidade)cbCidade.getSelectedItem());
        
        if (model.getIdCliente()!= 0) {
            dao.update(model);
        } else {
            dao.insert(model);
        }
        dispose();
    }//GEN-LAST:event_btSalvarActionPerformed

