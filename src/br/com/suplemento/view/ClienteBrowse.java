private void openForm(Cliente model) {
        if (form == null)
            form = new ClienteForm();
        form.setModel(model);
        form.setVisible(true);
    }

    private long getSelectedId() {
        if (tbClientes.getSelectedRow() == -1)
            return 0;
        
        Object value = tbClientes.getModel().getValueAt(
                    tbClientes.getSelectedRow(), 0);
        return (Long) value;
    }
