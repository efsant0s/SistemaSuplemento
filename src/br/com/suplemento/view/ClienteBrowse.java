    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
     if (getSelectedId() != 0) {
            int resposta = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o registro?");
            if (resposta == JOptionPane.YES_OPTION)
                dao.delete(getSelectedId());
        }
    }//GEN-LAST:event_btExcluirActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
    List<Cliente> clientes = dao.findAll();
        
        String[] columnNames = new String[] {"ID", "Codigo", "Nome", "Telefone", "Email", "Cidade"};
        
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        
        for (Cliente cliente : clientes) {
            Object[] dados = new Object[]{cliente.getIdCliente(), cliente.getCodigo(), cliente.getNome(),
            cliente.getTelefone(), cliente.getEmail(), 
            cliente.getCidade()== null ?"":cliente.getCidade().getNome()
            };
            
            model.addRow(dados);
        }
        tbClientes.setModel(model);
    }//GEN-LAST:event_formWindowActivated
public void abrirJanela(String nome) {
        JFrame frame = null;
        if (janelas.containsKey(nome)) {
            frame = janelas.get(nome);
        } else {
            switch (nome) {
                case "EstadoBrowse":
                    frame = new EstadoBrowse();
                    break;
                case "CidadeBrowse":
                    frame = new CidadeBrowse();
                    break;
                
                    
                default:
                    throw new AssertionError("Janela não existe");
            }
        }
        frame.setVisible(true);
        janelas.put(nome, frame);
    }
