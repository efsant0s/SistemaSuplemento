    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
    model.setCodigo(edCodigo.getText());
        model.setNome(edNome.getText());
        model.setEstado((Estado)cbEstado.getSelectedItem());
        
        if (model.getIdCidade()!= 0) {
            daoCidade.update(model);
        } else {
            daoCidade.insert(model);
        }
        dispose();
            
    }//GEN-LAST:event_btSalvarActionPerformed

    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        cbEstado.removeAllItems();
        List<Estado> estado = daoEstado.findAll();
        for (Estado estados : estado) {
            cbEstado.addItem(estados);
        }
        cbEstado.setSelectedItem(model.getEstado());        // TODO add your handling code here:
    }//GEN-LAST:event_formWindowActivated

