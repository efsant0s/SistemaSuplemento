public void setModel(Cidade model) {
        this.model = model;
        edNome.setText(model.getNome());
        edCodigo.setText(model.getCodigo());
        if (model.getEstado()!= null) {
            Estado estado = daoEstado.find(model.getEstado().getIdEstado());
            cbEstado.setSelectedItem(estado);
        }
    }
